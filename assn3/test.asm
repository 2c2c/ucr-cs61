.orig x3000

LD R1,VALUE
BRn PRINTNEGSIGN
LD R0, NEGSIGN
OUT
LD R2,PTR
LDR R3,R2,#0 
FOR_EACH
   AND R4,R1,R3
   NOT R4,R4
   ADD R4,R4,#1 
   ADD R5,R3,R4
   BRnp ZEROBIT
   ONEBIT
       LD R0,ONE
       OUT
   ZEROBIT
       LD R0,ZERO
       OUT 
ADD R3,R3,#1
BRnp FOR_EACH



ZERO .FILL x30
ONE .FILL x31
NEGSIGN .FILL xF
VALUE .FILL x88888888
PTR .FILL x4000
SIZE .FILL x10
